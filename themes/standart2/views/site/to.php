<center><h1>Регресс за отсутствие техосмотра</h1></center>
<h2>В соответствии со ст. 14 ФЗ «Об ОСАГО» страховая компания имеет право предъявить регрессные требования (требования возместить понесенные расходы) к виновнику ДТП, если на момент аварии у него истек срок действия диагностической карты.</h2>
