<h1 style="color: white;font-size: 18px;">Что такое ОСАГО?</h1>
<h6 style="color: white;"><strong>ОСАГО</strong> — это действительно Обязательное Страхование Автогражданской Ответственности. Вот это не завуалированная формулировка. В эти четыре слова вложен смысл всего понятия в целом. То есть — покупая страховку ОСАГО, Вы защищаете себя от больших проблем в случае, если в произошедшем с Вами ДТП виноваты именно Вы. В данном случае за нанесенный Вами ущерб, вместо Вас расплатится страховая компания, у которой Вы купили полис. В случае прямо противоположной ситуации, если разбили Вашу машину, Вам оплатит ремонт страховая компания того человека, кто въехал в Вас.
